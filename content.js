// INJECT FUNCTION — do not edit below this line
// ═══════════════════════════════════════════════════

BY.inject = function(page) {
  // helper: set innerHTML safely
  function s(el, val) { if (el && val !== undefined) el.innerHTML = val; }
  // helper: get nth element by class
  function n(cls, i) { return document.querySelectorAll('.' + cls)[i]; }
  // helper: get first element by class
  function f(cls) { return document.querySelector('.' + cls); }

  // ─ GLOBAL: manifesto ─
  s(f('hero-manifesto'), BY.global.manifesto);
  s(f('ft-tag'), BY.global.tagline);
  s(f('ft-copy'), BY.global.copyright);

  // ─ PAGE-SPECIFIC ─
  const c = BY[page];
  if (!c) return;

  if (page === 'index') {
    // Hero
    s(f('hero-eyebrow'), c.hero.eyebrow);
    s(f('hero-desc'),    c.hero.desc);
    s(f('hero-float-1'), c.hero.floatTop);
    s(f('hero-float-2'), c.hero.floatBot);
    const ctaBtns = document.querySelectorAll('.hero-ctas .btn');
    if (ctaBtns[0]) ctaBtns[0].textContent = c.hero.cta1;
    if (ctaBtns[1]) ctaBtns[1].textContent = c.hero.cta2;

    // Why section
    s(f('why-title'), c.why.headline);
    c.why.cards.forEach((card, i) => {
      s(n('why-card-title', i), '<span>' + card.title + '</span>');
      s(n('why-card-desc',  i), card.desc);
    });

    // Who section
    s(f('who-title'), c.who.headline);
    c.who.cards.forEach((card, i) => {
      s(n('who-card-title', i), card.title + ' <span>' + card.hl + '</span>');
      s(n('who-card-desc',  i), card.desc);
    });

    // Stories section
    s(f('stories-title'), c.stories.headline);
    c.stories.episodes.forEach((ep, i) => {
      s(n('story-card-name', i), ep.ep);
      s(n('story-card-co',   i), ep.label);
      s(n('story-card-hook', i), ep.hook);
    });
    // Invite card (4th)
    const invName = document.querySelector('.story-card.invite .story-card-name');
    const invHook = document.querySelector('.story-card.invite .story-card-hook');
    s(invName, c.stories.invite.title);
    s(invHook, c.stories.invite.desc);

    // Final CTA
    s(f('final-title'), c.final.headline);
    s(f('final-sub'),   c.final.sub);
    const finalBtns = document.querySelectorAll('.final-card .btn');
    if (finalBtns[0]) finalBtns[0].textContent = c.final.cta1;
    if (finalBtns[1]) finalBtns[1].textContent = c.final.cta2;
  }

  if (page === 'about') {
    s(f('hero-title'), c.hero.title);
    s(f('hero-sub'),   c.hero.sub);
    s(f('founder-name'), c.founder.name);
    const bioEls = document.querySelectorAll('.bio-p');
    c.founder.bio.forEach((text, i) => { s(bioEls[i], text); });
  }

  if (page === 'club') {
    s(f('hero-title'), c.hero.title);
    s(f('hero-sub'),   c.hero.sub);
    s(f('hero-note'),  c.hero.note);
    c.benefits.cards.forEach((card, i) => {
      s(n('card-num',   i), card.num);
      s(n('card-title', i), card.title + ' <span>' + card.hl + '</span>');
      s(n('card-desc',  i), card.desc);
    });
    s(f('form-card-title'), c.form.title);
    s(f('form-submit'),     c.form.submit);
  }

  if (page === 'workshops') {
    s(f('hero-title'), c.hero.title);
    s(f('hero-sub'),   c.hero.sub);
    // Bullets
    const bullets = document.querySelectorAll('.hero-item');
    c.hero.bullets.forEach((txt, i) => {
      if (bullets[i]) {
        const icon = bullets[i].querySelector('.hero-item-icon');
        bullets[i].innerHTML = (icon ? icon.outerHTML : '') + ' ' + txt;
      }
    });
    // Topics
    s(f('sec-title') || f('topics-title'), c.topics.title);
    c.topics.cards.forEach((card, i) => {
      s(n('topic-card-num',   i), card.num);
      s(n('topic-card-title', i), card.title);
      s(n('topic-card-desc',  i), card.desc);
    });
    // Kit
    s(f('res-title'), c.kit.title);
    s(f('res-desc'),  c.kit.desc);
    const incs = document.querySelectorAll('.res-inc');
    c.kit.includes.forEach((txt, i) => {
      if (incs[i]) {
        const icon = incs[i].querySelector('.res-inc-icon');
        incs[i].innerHTML = (icon ? icon.outerHTML : '') + ' ' + txt;
      }
    });
  }
};

